class DriverInfoModel {
  String? id;
  String? name;
  String? email;
  String? carName;
  String? carPlateNum;
  String? carType;
  String? address;

  // Constructor with null safety
  DriverInfoModel({
    required this.id,
    required this.name,
    required this.email,
    required this.carName,
    required this.carPlateNum,
    required this.carType,
    required this.address,
  });
}
